package day3;

public class Carculator {
    static int plus(int num1, int num2){
        return num1 + num2;
    }
    static int minus(int num1, int num2){
        return num1 - num2;
    }
    static int divide(int num1, int num2){
        return num1/ num2;
    }
    static int multiple(int num1, int num2){
        return num1 * num2;
    }
    static int square(int num1){
        return num1 * num1;
    }
}

