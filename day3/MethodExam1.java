package day3;

public class MethodExam1 {
    public static void hi() {
        System.out.println("안녕하세요. 저는 홍길동입니다.잘 부탁드립니다.");
    }
    public static void bi(){
        System.out.println("안녕히 가세요. 다음에 또 봐요!");
    }
    public static void hi1() {
        System.out.println("반갑습니다.");
    }
    public static void hi2() {
        System.out.println("국적은 대한민국입니다.");
    }
    public static void hi3(){
        System.out.println("취미는 자바를 통한");
    }
    public static void hi4(){
        System.out.println("프로그래밍 입니다.");
    }
    public static void main(String[] args) {

        hi();
        hi1();
        hi2();
        hi3();
        hi4();
        bi();


    }
}
